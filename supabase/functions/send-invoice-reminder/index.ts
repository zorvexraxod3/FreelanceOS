import "jsr:@supabase/functions-js/edge-runtime.d.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Methods": "GET, POST, PUT, DELETE, OPTIONS",
  "Access-Control-Allow-Headers": "Content-Type, Authorization, X-Client-Info, Apikey",
};

Deno.serve(async (req: Request) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { status: 200, headers: corsHeaders });
  }

  try {
    const { clientEmail, clientName, invoiceNumber, amount } = await req.json();

    if (!clientEmail || !invoiceNumber) {
      return new Response(
        JSON.stringify({ error: "Missing required fields" }),
        { status: 400, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    console.log(`[EMAIL] To: ${clientEmail}`);
    console.log(`[EMAIL] Subject: Payment Reminder: Invoice ${invoiceNumber}`);
    console.log(`[EMAIL] Body: Hi ${clientName}, this is a reminder that invoice ${invoiceNumber} for $${amount} is overdue. Please make payment as soon as possible.`);

    return new Response(
      JSON.stringify({ success: true, message: "Invoice reminder queued" }),
      { headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  } catch (err) {
    return new Response(
      JSON.stringify({ error: err.message }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
